    ("AUTH_DB_PATH", "SQLite auth database path inside the container"),
    ("AUTH_JWT_SECRET", "JWT signing secret generated by the installer"),
    ("AUTH_JWT_TTL", "JWT lifetime accepted by solr-search/auth.py (e.g. 24h)"),
    ("AUTH_COOKIE_NAME", "HTTP cookie name used for browser auth"),
    ("AUTH_ADMIN_USERNAME", "Installer-managed bootstrap admin username"),
    ("RABBITMQ_USER", "RabbitMQ broker/admin username used by Compose and admin surfaces"),
    ("RABBITMQ_PASS", "RabbitMQ broker/admin password generated by the installer"),
    ("REDIS_PASSWORD", "Redis password applied via redis-server --requirepass"),
    ("VERSION", "Container build metadata (defaults to VERSION file)"),
    ("GIT_COMMIT", "Container build metadata (defaults to current HEAD)"),
