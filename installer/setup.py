def resolve_value(
    *,
    provided: str | None,
    existing: str | None,
    interactive: bool,
    prompt_label: str,
    validator: Callable[[str], object],
    flag_name: str,
):
    if provided is not None:
        return validator(provided)

    if interactive:
        while True:
            raw_value = prompt_text(prompt_label, default=existing)
            try:
                return validator(raw_value)
            except ValueError as exc:
                print(f"Error: {exc}")

    if existing is not None:
        return validator(existing)

    raise ValueError(f"{flag_name} is required when stdin is not interactive")


def resolve_password(*, provided: str | None, interactive: bool, can_keep_existing: bool) -> str | None:
    if provided is not None:
        if not provided:
            raise ValueError("--admin-password cannot be empty")
        return provided

    if not interactive:
        if can_keep_existing:
            return None
        raise ValueError("--admin-password is required when stdin is not interactive")

    return prompt_password(can_keep_existing=can_keep_existing)


def prompt_text(label: str, *, default: str | None = None) -> str:
    suffix = f" [{default}]" if default else ""
    raw_value = input(f"{label}{suffix}: ").strip()
    if raw_value:
        return raw_value
    if default is not None:
        return default
    return raw_value


def prompt_password(*, can_keep_existing: bool) -> str | None:
    while True:
        prompt = "Admin password"
        if can_keep_existing:
            prompt += " (leave blank to keep the existing password)"
        prompt += ": "
        password = getpass.getpass(prompt)
        if not password and can_keep_existing:
            return None
        if not password:
            print("Error: Admin password is required")
            continue
        confirmation = getpass.getpass("Confirm admin password: ")
        if password != confirmation:
            print("Error: Passwords do not match")
            continue
        return password


def read_repo_version() -> str:
    version_path = ROOT / "VERSION"
    if not version_path.exists():
        return "dev"
    return version_path.read_text(encoding="utf-8").strip() or "dev"


def read_git_commit() -> str:
    git_executable = shutil.which("git")
    if git_executable is None:
        return "unknown"

    completed = subprocess.run(  # noqa: S603
        [git_executable, "rev-parse", "HEAD"],
        cwd=ROOT,
        capture_output=True,
        check=False,
        text=True,
    )
    commit = completed.stdout.strip()
    return commit or "unknown"


def utc_now_iso() -> str:
    return datetime.now(UTC).replace(microsecond=0).isoformat().replace("+00:00", "Z")


def _quote_env_value(value: str) -> str:
    sanitized = value.replace("\n", "").replace("\r", "").replace("\x00", "")
    if any(character.isspace() for character in sanitized) or "#" in sanitized:
        escaped = sanitized.replace("\\", "\\\\").replace('"', '\\"')
        return f'"{escaped}"'
    return sanitized


def _unquote_env_value(value: str) -> str:
    if len(value) >= 2 and value[0] == value[-1] and value[0] in {'"', "'"}:
        inner = value[1:-1]
        if value[0] == '"':
            return inner.replace('\\"', '"').replace("\\\\", "\\")
        return inner
    return value


def ensure_runtime_dependencies(argv: list[str]) -> None:
    try:
        from aithena_common.passwords import hash_password as _probe  # noqa: PLC0415, F401
    except ImportError as exc:
        if os.environ.get("AITHENA_INSTALLER_UV") == "1":
            raise RuntimeError("aithena-common is not installed. Run `cd installer && uv sync` first.") from exc

        uv_executable = shutil.which("uv")
        if uv_executable is None:
            raise RuntimeError("aithena-common is not installed and uv is not available.") from exc

        completed = subprocess.run(  # noqa: S603
            [
                uv_executable,
                "run",
                "--project",
                str(ROOT / "installer"),
                "python",
                "-m",
                "installer.setup",
                *argv,
            ],
            cwd=ROOT,
            check=False,
            env={**os.environ, "AITHENA_INSTALLER_UV": "1"},
        )
        raise SystemExit(completed.returncode) from exc


